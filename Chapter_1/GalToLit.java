class GalToLit {
    public static void main(String[] args) {
        int Gallons;
        double Liters;

        Gallons = 10;

        System.out.println("Начальное значение переменной Gallons: " + Gallons);
        Liters = Gallons * 3.7854;
        System.out.println(Gallons + " галлонам соответствует " + Liters + " литров");
    }
}
