// Демон-ия использ-ия цикла while
class WhileDemo {
    public static void main(String[] args) {
        char ch = 'a';

        // вывести буквы английского алфавита, исп-ия цикл while.
        while(ch <= 'z') {
            System.out.print(ch + " ");
            ch++;
        }
    }
}
